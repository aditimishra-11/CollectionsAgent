def main() -> None:
    from datetime import date as _date
    ap = argparse.ArgumentParser()
    ap.add_argument("--only", help="Comma-separated call_ids to grade (optional)")
    ap.add_argument("--since", default=_date.today().isoformat(), help="ISO date floor (default: today)")
    args = ap.parse_args()

    # Default behaviour: grade every call in logs/. Auto-annotation kicks in
    # for any call without a hand-written entry. Future calls are graded the
    # moment their JSONL hits logs/ — zero manual prep step.
    assert_runtime_keys()
    explicit_annotations = yaml.safe_load(ANNOTATIONS_FILE.read_text(encoding="utf-8"))["calls"] or []
    explicit_by_id = {a["call_id"]: a for a in explicit_annotations}

    if args.only:
        wanted = set(args.only.split(","))
        all_call_ids = [c for c in discover_all_calls(args.since) if c in wanted]
    else:
        all_call_ids = discover_all_calls(args.since)

    annotations = []
    for cid in all_call_ids:
        if cid in explicit_by_id:
            annotations.append(explicit_by_id[cid])
        else:
            try:
                started, _, _ = load_call(cid)
                annotations.append(auto_annotation(cid, started, {}))
            except Exception as e:
                logger.warning(f"Skipping {cid}: {e}")
    explicit_overlap = sum(1 for cid in all_call_ids if cid in explicit_by_id)
    logger.info(
        f"Discovered {len(all_call_ids)} calls since {args.since}. "
        f"{explicit_overlap} have explicit annotations; "
        f"{len(annotations) - explicit_overlap} auto-annotated from persona."
    )

    judge = LLMJudge()
    results: list[CallEvalResult] = []
    for ann in annotations:
        is_auto = ann.get("_auto", False)
        tag = " [AUTO]" if is_auto else " [EXPLICIT]"
        logger.info(f"Grading call {ann['call_id']} ({ann.get('persona_id', '?')}){tag}")
        try:
            r = grade_one(ann, judge)
            results.append(r)
            logger.info(
                f"  → full_pass={r.full_pass} outcome={r.outcome_match} "
                f"comp={r.compliance_pass} tone={r.tone_pass} hal={r.hallucination_pass} "
                f"close={r.closure_coherence_pass} contract={r.contract_consistency_pass}"
            )
            # Incremental write — every grade is persisted immediately so a
            # mid-run API failure (rate-limit, network, etc.) doesn't lose work.
            write_csv(results)
        except FileNotFoundError as e:
            logger.warning(f"  Skipping (no log file): {e}")
        except Exception as e:
            # Don't kill the whole run on a single call's API error. Log,
            # persist what we have so far, continue to next call. If it's
            # a quota exhaustion every subsequent call will hit it too —
            # surfaced clearly in the log.
            logger.error(f"  Grade failed: {type(e).__name__}: {e}")
            logger.error("  Continuing to next call. Re-run after issue is resolved to grade remainder.")

    write_csv(results)
    print_summary(results)
    if len(results) < len(annotations):
        print(f"\nNOTE: only {len(results)} of {len(annotations)} annotations graded successfully — "
              "see error log above. Re-run after resolving the underlying issue to grade the rest.")


if __name__ == "__main__":
    main()
