"""Real-call eval runner — grades actual JSONL call logs against per-call annotations.

Complements the synthetic ``eval/runner.py`` (which generates conversations from
``scenarios.yaml``). The shape is intentionally parallel:

    Synthetic eval                        Real-call eval
    -------------------------------       -------------------------------
    eval/scenarios.yaml                   eval/annotations_live.yaml
    eval/runner.py                        eval/runner_live.py
    eval/results_v2.csv                   eval/results_live.csv

Same grading axes (compliance, outcome_match, tone, hallucination,
bot_must / bot_must_not, Likert empathy, closure_coherence, contract_consistency).
Same GPT-4o-as-judge methodology. The only difference: the customer turns aren't
generated by the eval; they come from a real call's STT transcript saved in
``logs/call_<id>.jsonl``.

Annotations can be EXPLICIT (hand-written in annotations_live.yaml — full
grading including outcome_match, bot_must, bot_must_not for a specific call)
or AUTO-GENERATED (--all mode: derives a baseline annotation from the
persona's segment policy + universal bot-speech rules; grades everything
EXCEPT outcome_match, which has no ground truth without human annotation).

Why this exists: synthetic scenarios are useful but can be misleading — the
"customer" is a GPT-4o roleplay, not a real Indian English speaker with STT
noise and natural phrasing. Real-call grading complements the synthetic numbers
with evidence from actual calls. The headline writeup metrics stand on both
legs: 42 synthetic × 5 cycles AND N real calls (today: 3 explicit + 24
auto-graded).

Usage:
    python -m eval.runner_live              # explicit annotations only
    python -m eval.runner_live --only 04984f7ee1
    python -m eval.runner_live --all         # every call in logs/ (today)
    python -m eval.runner_live --all --since 2026-05-15
"""

