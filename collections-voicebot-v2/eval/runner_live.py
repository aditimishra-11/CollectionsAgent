# Map persona strategy → tone_check key the judge understands.
_STRATEGY_TO_TONE: dict[str, str] = {
    "apex_concierge":      "apex_concierge",
    "A_reminder":          "a_reminder",
    "B_problem_solving":   "b_problem_solving",
}


def auto_annotation(call_id: str, started: dict, personas_by_id: dict) -> dict:
    """Build a baseline annotation for a call WITHOUT a hand-written entry.

    Derives tone_check from the persona's pre-filter strategy. Uses the
    universal bot_must / bot_must_not lists. Skips outcome_match (set
    expected_outcome=None) since we have no ground truth for ad-hoc calls.
    Auto-annotations grade compliance + tone + hallucination + closure +
    contract + universal behavioural rules — a meaningful partial score.
    """
    persona_id = started.get("customer_id", "?")
    strategy = started.get("strategy", "")
    tone_check = _STRATEGY_TO_TONE.get(strategy, "b_problem_solving")
    return {
        "call_id": call_id,
        "persona_id": persona_id,
        "description": f"Auto-annotated from persona {persona_id} (strategy={strategy})",
        "expected_outcome": None,    # signals: do not grade outcome_match
        "expected_slots": [],
        "bot_must": list(_UNIVERSAL_BOT_MUST),
        "bot_must_not": list(_UNIVERSAL_BOT_MUST_NOT),
        "tone_check": tone_check,
        "weight": 1.0,
        "_auto": True,
    }


def discover_all_calls(since_date: str) -> list[str]:
    """Return all call_ids in logs/ whose JSONL mtime is on or after since_date.
    Dates are YYYY-MM-DD ISO strings.
    """
    from datetime import datetime
    out = []
    for p in LOGS_DIR.glob("call_*.jsonl"):
        mtime_date = datetime.fromtimestamp(p.stat().st_mtime).date().isoformat()
        if mtime_date >= since_date:
            call_id = p.stem.replace("call_", "")
            out.append(call_id)
    return sorted(out)


