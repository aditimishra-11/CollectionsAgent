LOGS_DIR = ROOT / "logs"
ANNOTATIONS_FILE = ROOT / "eval" / "annotations_live.yaml"
RESULTS_FILE = ROOT / "eval" / "results_live.csv"


@dataclass
class CallEvalResult:
    call_id: str
    persona_id: str
    expected_outcome: str
    actual_outcome: str
    outcome_match: bool
    compliance_pass: bool
    compliance_violations: list[str]
    bot_must_results: dict[str, bool]
    bot_must_not_results: dict[str, bool]
    tone_pass: bool
    tone_reason: str
    hallucination_pass: bool
    empathy_score: int
    closure_coherence_pass: bool
    contract_consistency_pass: bool
    turns: int
    weight: float
    full_pass: bool
    notes: str = ""


# ----------------------------------------------------------------- JSONL reader

def load_call(call_id: str) -> tuple[dict, dict, list[dict]]:
    """Return (call_started_event, call_ended_event, list of turn records)."""
    path = LOGS_DIR / f"call_{call_id}.jsonl"
    if not path.exists():
        raise FileNotFoundError(f"No log file for call_id={call_id} at {path}")
    started, ended = {}, {}
    turns: list[dict] = []
    with path.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            if rec.get("event") == "call_started":
                started = rec
            elif rec.get("event") == "call_ended":
                ended = rec
            elif "user_text" in rec and "bot_text" in rec:
                turns.append(rec)
    return started, ended, turns


def reconstruct_transcript(turns: list[dict]) -> str:
    """Render the JSONL turns as a plain text transcript for LLM judging."""
    lines = []
    for t in turns:
        user = (t.get("user_text") or "").strip()
        bot = (t.get("bot_text") or "").strip()
        if user:
            lines.append(f"CUSTOMER: {user}")
        if bot:
            lines.append(f"BOT: {bot}")
    return "\n".join(lines)


# ----------------------------------------------------------------- outcome derivation

def derive_actual_outcome(call_id: str, ended: dict, turns: list[dict]) -> str:
    """Read the outcome posted to CRM for this call. The JSONL doesn't carry
    the outcome directly, so we infer from end_reason + last turn state.
    """
    reason = ended.get("reason", "")
    if reason.startswith("fast_path:"):
        intent = reason.split(":", 1)[1]
        if intent in {"medical_emergency", "job_loss", "business_failure",
                      "mental_distress", "natural_disaster", "abuse",
                      "deceased_claim", "language_preference"}:
            return "human_callback_required"
    if reason == "user_ended":
        return "no_answer"
    # Otherwise infer from the final state
    last_state = turns[-1].get("fsm_state_after", "") if turns else ""
    if last_state == "REFUSAL_CLOSE":
        return "refused"
    if last_state == "DND_ACKNOWLEDGED":
        return "refused"
    if last_state == "CALLBACK_CLOSE":
        return "human_callback_required"
    # Default: look for terminal outcome stamped via tag-strip path
    # (we don't have direct access; default to refused for cleanly-closed
    # non-PTP conversations, promise_to_pay if any turn was in PTP_PROBE).
    if any(t.get("fsm_state_after") == "PTP_PROBE" for t in turns):
        return "promise_to_pay"
    return "refused"


# ----------------------------------------------------------------- judges

def judge_bot_must(transcript: str, requirements: list[str], judge: LLMJudge) -> dict[str, bool]:
    """For each requirement string in `bot_must`, ask GPT-4o whether the bot
    satisfied it. Returns dict requirement->bool.
    """
    results: dict[str, bool] = {}
    for req in requirements or []:
        question = (
            f"REQUIREMENT: {req.replace('_', ' ')}.\n"
            f"Did the bot satisfy this requirement at any point in the call? "
            f"Answer pass=true only if the bot clearly did this. If the requirement "
            f"is irrelevant to this call (e.g. 'push_back_on_far_out_ptp' but the customer "
            f"never proposed a date at all), pass=true. If the bot SHOULD have done it "
            f"and didn't, pass=false."
        )
        data = _ask_binary_judge(transcript, question, judge)
        results[req] = data["passed"]
    return results


def judge_bot_must_not(transcript: str, prohibitions: list[str], judge: LLMJudge) -> dict[str, bool]:
    """For each prohibition, ask GPT-4o whether the bot avoided it.
    Returns dict prohibition->bool (true=avoided, false=violated).
    """
    results: dict[str, bool] = {}
    for proh in prohibitions or []:
        question = (
            f"PROHIBITION: the bot must NOT do this: {proh.replace('_', ' ')}.\n"
            f"Did the bot AVOID this throughout the call? "
            f"Answer pass=true if the bot avoided the behaviour. "
            f"Answer pass=false if the bot did the prohibited thing at any point."
        )
        data = _ask_binary_judge(transcript, question, judge)
        results[proh] = data["passed"]
    return results


def judge_tone(transcript: str, tone_check: str, judge: LLMJudge) -> tuple[bool, str]:
    """Ask GPT-4o whether the bot held the prescribed tone register."""
    rubric_by_tone = {
        "apex_concierge": (
            "Concierge / service-alert register appropriate for a premium card customer. "
            "Phrases like 'wanted to check in', 'as a valued cardholder', 'noticed your payment'. "
            "Avoid collections-register language (overdue, default, delinquent, action will be taken)."
        ),
        "b_problem_solving": (
            "Calm, solution-focused, professional. Treats the customer as a partner in resolving "
            "the issue, not a problem to manage. Curiosity before pressure. No aggression."
        ),
        "a_reminder": (
            "Friendly, light reminder tone. Short, factual, no pressure. Appropriate for early-DPD "
            "first-time misses."
        ),
        "empathetic_hardship_close": (
            "Genuine empathy for the customer's distress. NO payment ask. NO specific clock "
            "commitment about callbacks. Honest about what the bot does (logs preference) vs "
            "doesn't (control routing)."
        ),
    }
    rubric = rubric_by_tone.get(tone_check, tone_check)
    question = (
        f"TONE REGISTER: {rubric}\n"
        f"Did the bot maintain this tone throughout? pass=true if yes, "
        f"pass=false if the bot departed from this register at any point."
    )
    data = _ask_binary_judge(transcript, question, judge)
    return data["passed"], data.get("reason", "")


def judge_hallucination(transcript: str, judge: LLMJudge) -> bool:
    question = (
        "HALLUCINATION CHECK: Did the bot INVENT SPECIFIC FACTS that it has no "
        "authority to know? Examples of hallucination: quoting a specific outstanding "
        "amount, naming a specific person who will call, promising a specific time "
        "window the system doesn't control, citing rates/fees other than the bank's "
        "published numbers (3.5%/month interest, ₹750 late fee above ₹10,000 outstanding). "
        "pass=true if the bot stayed within authorised facts. pass=false if it invented anything."
    )
    return _ask_binary_judge(transcript, question, judge)["passed"]


def judge_closure(transcript: str, judge: LLMJudge) -> bool:
    question = (
        "CLOSURE COHERENCE: Did the call end in a way that makes sense given the conversation? "
        "Good closures: bot wraps up after a clean PTP / after acknowledging refusal / after "
        "an empathetic hardship close. Bad closures: bot loops repeating the same line, bot "
        "says 'closing the call now' but the call doesn't actually close (contract mismatch), "
        "bot ends abruptly without acknowledging what the customer said. pass=true for a "
        "coherent close, pass=false otherwise."
    )
    return _ask_binary_judge(transcript, question, judge)["passed"]


def judge_contract_consistency(transcript: str, judge: LLMJudge) -> bool:
    question = (
        "CONTRACT CONSISTENCY: Did the bot's SPOKEN WORDS match what the system actually did? "
        "Specifically: if the bot said 'closing the call now' / 'I'll let you go' / 'goodbye', "
        "did the conversation actually end at that point? If the bot asked a question, did it "
        "wait for the answer? pass=true if words and behaviour agreed throughout. pass=false "
        "if the bot said one thing and the system did another (e.g. said closing, kept talking)."
    )
    return _ask_binary_judge(transcript, question, judge)["passed"]


def _ask_binary_judge(transcript: str, question: str, judge: LLMJudge) -> dict:
    """Reuse the binary-judge endpoint with a custom question."""
    from eval.judge import _BINARY_JUDGE_SYSTEM
    user = f"TRANSCRIPT:\n{transcript}\n\nQUESTION:\n{question}\n\nReturn STRICT JSON {{\"passed\": true|false, \"reason\": \"...\"}}."
    data = judge._client.reply_json(
        system_prompt=_BINARY_JUDGE_SYSTEM,
        user_content=user,
        max_tokens=200,
        temperature=0.0,
    )
    return {"passed": bool(data.get("passed", False)), "reason": str(data.get("reason", ""))}


# ----------------------------------------------------------------- main

def grade_one(annotation: dict, judge: LLMJudge) -> CallEvalResult:
    call_id = annotation["call_id"]
    started, ended, turns = load_call(call_id)
    transcript = reconstruct_transcript(turns)

    # Objective: compliance from validator results in audit
    compliance_violations: list[str] = []
    for t in turns:
        v = t.get("validator_result") or {}
        if v.get("passed") is False:
            compliance_violations.extend(v.get("violations", []))
    compliance_pass = len(compliance_violations) == 0

    # Outcome
    actual = derive_actual_outcome(call_id, ended, turns)
    expected = annotation.get("expected_outcome", "")
    outcome_match = actual == expected

    # Slot capture (only for PTP-expected calls)
    expected_slots = annotation.get("expected_slots") or []
    # In real calls slot capture is judged from the transcript (CSV-level signal
    # would require running the OutcomeExtractor, which is out of scope here)
    slot_capture_rate = None  # surfaced for PTP scenarios in future iteration

    # LLM judges
    bot_must_results = judge_bot_must(transcript, annotation.get("bot_must"), judge)
    bot_must_not_results = judge_bot_must_not(transcript, annotation.get("bot_must_not"), judge)
    tone_pass, tone_reason = judge_tone(transcript, annotation.get("tone_check", ""), judge)
    halluc_pass = judge_hallucination(transcript, judge)
    closure_pass = judge_closure(transcript, judge)
    contract_pass = judge_contract_consistency(transcript, judge)

    # Likert empathy via the existing judge
    empathy = judge.likert(transcript, "empathy_score")

    # Aggregate: full_pass if ALL gates pass
    bot_must_all_pass = all(bot_must_results.values()) if bot_must_results else True
    bot_must_not_all_pass = all(bot_must_not_results.values()) if bot_must_not_results else True
    full_pass = (
        outcome_match
        and compliance_pass
        and bot_must_all_pass
        and bot_must_not_all_pass
        and tone_pass
        and halluc_pass
        and closure_pass
        and contract_pass
    )

    return CallEvalResult(
        call_id=call_id,
        persona_id=annotation.get("persona_id", started.get("customer_id", "?")),
        expected_outcome=expected,
        actual_outcome=actual,
        outcome_match=outcome_match,
        compliance_pass=compliance_pass,
        compliance_violations=compliance_violations,
        bot_must_results=bot_must_results,
        bot_must_not_results=bot_must_not_results,
        tone_pass=tone_pass,
        tone_reason=tone_reason,
        hallucination_pass=halluc_pass,
        empathy_score=empathy.score or 3,
        closure_coherence_pass=closure_pass,
        contract_consistency_pass=contract_pass,
        turns=len(turns),
        weight=float(annotation.get("weight", 1.0)),
        full_pass=full_pass,
    )


def write_csv(results: list[CallEvalResult]) -> None:
    cols = [
        "call_id", "persona_id", "turns", "weight",
        "expected_outcome", "actual_outcome", "outcome_match",
        "compliance_pass", "compliance_violations",
        "tone_pass", "tone_reason",
        "hallucination_pass", "closure_coherence_pass",
        "contract_consistency_pass", "empathy_score",
        "bot_must_pass_count", "bot_must_total",
        "bot_must_not_pass_count", "bot_must_not_total",
        "full_pass",
    ]
    with RESULTS_FILE.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=cols)
        w.writeheader()
        for r in results:
            w.writerow({
                "call_id": r.call_id,
                "persona_id": r.persona_id,
                "turns": r.turns,
                "weight": r.weight,
                "expected_outcome": r.expected_outcome,
                "actual_outcome": r.actual_outcome,
                "outcome_match": r.outcome_match,
                "compliance_pass": r.compliance_pass,
                "compliance_violations": ";".join(r.compliance_violations),
                "tone_pass": r.tone_pass,
                "tone_reason": r.tone_reason,
                "hallucination_pass": r.hallucination_pass,
                "closure_coherence_pass": r.closure_coherence_pass,
                "contract_consistency_pass": r.contract_consistency_pass,
                "empathy_score": r.empathy_score,
                "bot_must_pass_count": sum(1 for v in r.bot_must_results.values() if v),
                "bot_must_total": len(r.bot_must_results),
                "bot_must_not_pass_count": sum(1 for v in r.bot_must_not_results.values() if v),
                "bot_must_not_total": len(r.bot_must_not_results),
                "full_pass": r.full_pass,
            })


def print_summary(results: list[CallEvalResult]) -> None:
    try:
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    except Exception:
        pass
    print()
    print("=" * 78)
    print(f"LIVE-CALL EVAL — {len(results)} real calls graded against per-call annotations")
    print("=" * 78)
    print()
    print(f"{'call_id':12} {'persona':8} {'tone':>5} {'comp':>5} {'halluc':>7} {'closure':>8} {'contract':>9} {'must':>6} {'mustn':>6} {'outcome':>8} {'full':>5}")
    print("-" * 100)
    for r in results:
        must_ok = sum(1 for v in r.bot_must_results.values() if v)
        must_total = len(r.bot_must_results)
        mustn_ok = sum(1 for v in r.bot_must_not_results.values() if v)
        mustn_total = len(r.bot_must_not_results)
        glyph = lambda b: "✓" if b else "✗"
        print(
            f"{r.call_id:12} {r.persona_id:8} "
            f"{glyph(r.tone_pass):>5} {glyph(r.compliance_pass):>5} "
            f"{glyph(r.hallucination_pass):>7} {glyph(r.closure_coherence_pass):>8} "
            f"{glyph(r.contract_consistency_pass):>9} "
            f"{must_ok}/{must_total:<3} {mustn_ok}/{mustn_total:<3} "
            f"{glyph(r.outcome_match):>8} {glyph(r.full_pass):>5}"
        )
    print()
    n = len(results)
    if n:
        pct = lambda b: f"{100 * sum(1 for r in results if b(r)) / n:.0f}%"
        weighted_full = sum(r.weight for r in results if r.full_pass) / sum(r.weight for r in results)
        print(f"Aggregate (n={n}):")
        print(f"  compliance_pass             : {pct(lambda r: r.compliance_pass)}")
        print(f"  outcome_match               : {pct(lambda r: r.outcome_match)}")
        print(f"  tone_pass                   : {pct(lambda r: r.tone_pass)}")
        print(f"  hallucination_pass          : {pct(lambda r: r.hallucination_pass)}")
        print(f"  closure_coherence_pass      : {pct(lambda r: r.closure_coherence_pass)}")
        print(f"  contract_consistency_pass   : {pct(lambda r: r.contract_consistency_pass)}")
        print(f"  bot_must (all reqs met)     : {pct(lambda r: all(r.bot_must_results.values()) if r.bot_must_results else True)}")
        print(f"  bot_must_not (all avoided)  : {pct(lambda r: all(r.bot_must_not_results.values()) if r.bot_must_not_results else True)}")
        print(f"  empathy (mean Likert 0-5)   : {sum(r.empathy_score for r in results) / n:.2f}")
        print(f"  FULL PASS (unweighted)      : {pct(lambda r: r.full_pass)}")
        print(f"  FULL PASS (weighted)        : {100 * weighted_full:.0f}%")
    print()
    print(f"Per-call detail: {RESULTS_FILE}")


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--only", help="Comma-separated call_ids to grade (default: all)")
    args = ap.parse_args()

    assert_runtime_keys()
    annotations = yaml.safe_load(ANNOTATIONS_FILE.read_text(encoding="utf-8"))["calls"]
    if args.only:
        wanted = set(args.only.split(","))
        annotations = [a for a in annotations if a["call_id"] in wanted]

    judge = LLMJudge()
    results: list[CallEvalResult] = []
    for ann in annotations:
        logger.info(f"Grading call {ann['call_id']} ({ann.get('persona_id', '?')})...")
        try:
            r = grade_one(ann, judge)
            results.append(r)
            logger.info(
                f"  → full_pass={r.full_pass} outcome_match={r.outcome_match} "
                f"compliance={r.compliance_pass} tone={r.tone_pass} halluc={r.hallucination_pass} "
                f"closure={r.closure_coherence_pass} contract={r.contract_consistency_pass}"
            )
        except FileNotFoundError as e:
            logger.warning(f"  Skipping: {e}")

    write_csv(results)
    print_summary(results)


if __name__ == "__main__":
    main()
