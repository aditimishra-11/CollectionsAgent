"""Real-call eval runner — grades actual JSONL call logs against per-call annotations.

Complements the synthetic ``eval/runner.py`` (which generates conversations from
``scenarios.yaml``). The shape is intentionally parallel:

    Synthetic eval                        Real-call eval
    -------------------------------       -------------------------------
    eval/scenarios.yaml                   eval/annotations_live.yaml
    eval/runner.py                        eval/runner_live.py
    eval/results_v2.csv                   eval/results_live.csv

Same grading axes (compliance, outcome_match, slot_capture, tone, hallucination,
bot_must / bot_must_not, plus Likert empathy / sentiment / context). Same
GPT-4o-as-judge methodology. The only difference: the customer turns aren't
generated by the eval; they come from a real call's STT transcript saved in
``logs/call_<id>.jsonl``.

Why this exists: synthetic scenarios are useful but can be misleading — the
"customer" is a GPT-4o roleplay, not a real Indian English speaker with STT
noise and natural phrasing. Real-call grading complements the synthetic numbers
with evidence from actual calls. The headline writeup metrics stand on both
legs: 42 synthetic × 5 cycles AND N real calls (n=3 today, expandable).

Usage:
    python -m eval.runner_live
    python -m eval.runner_live --only 04984f7ee1
"""

